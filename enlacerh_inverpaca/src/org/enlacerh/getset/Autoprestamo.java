package org.enlacerh.getset;

public class Autoprestamo {
	   private String vanio;
	   private String vmes;
	   private String vcodcto;
	   private String vdescto;
	   private String vmonto;
	   private String vsaldo;
	/**
	 * @return the vanio
	 */
	public String getVanio() {
		return vanio;
	}
	/**
	 * @param vanio the vanio to set
	 */
	public void setVanio(String vanio) {
		this.vanio = vanio;
	}
	/**
	 * @return the vmes
	 */
	public String getVmes() {
		return vmes;
	}
	/**
	 * @param vmes the vmes to set
	 */
	public void setVmes(String vmes) {
		this.vmes = vmes;
	}
	/**
	 * @return the vcodcto
	 */
	public String getVcodcto() {
		return vcodcto;
	}
	/**
	 * @param vcodcto the vcodcto to set
	 */
	public void setVcodcto(String vcodcto) {
		this.vcodcto = vcodcto;
	}
	/**
	 * @return the vdescto
	 */
	public String getVdescto() {
		return vdescto;
	}
	/**
	 * @param vdescto the vdescto to set
	 */
	public void setVdescto(String vdescto) {
		this.vdescto = vdescto;
	}
	/**
	 * @return the vmonto
	 */
	public String getVmonto() {
		return vmonto;
	}
	/**
	 * @param vmonto the vmonto to set
	 */
	public void setVmonto(String vmonto) {
		this.vmonto = vmonto;
	}
	/**
	 * @return the vsaldo
	 */
	public String getVsaldo() {
		return vsaldo;
	}
	/**
	 * @param vsaldo the vsaldo to set
	 */
	public void setVsaldo(String vsaldo) {
		this.vsaldo = vsaldo;
	}
	   
	   
}
